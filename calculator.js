while (true) {
    // Prompt for the first number
    let x = parseFloat(prompt("Value of x"));
    if (isNaN(x)) {
        alert("Invalid input. Please enter a valid number.");
        continue;
    }

    let y = parseFloat(prompt("Value of y"));
    if (isNaN(y)) {
        alert("Invalid input. Please enter a valid number.");
        continue;
    }


}